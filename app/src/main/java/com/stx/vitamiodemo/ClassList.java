package com.stx.vitamiodemo;

public class ClassList {

    private String name;

    private int imageId;

    public ClassList(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
